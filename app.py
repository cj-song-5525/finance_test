import streamlit as st

# 1. 페이지 설정 및 제목
st.set_page_config(page_title="Investment Style Test", page_icon="📈")
st.title("🛡️ 나의 투자 성향 자가 진단")
st.write("---")

# 2. 주의 사항
st.warning("""
**[주의 사항]**
* 본 테스트는 교육용으로 만들어진 간단한 질문지입니다.
* 본 테스트 결과를 이용하여 실제 투자를 하는 것은 위험합니다.
* 실제 투자는 원금 손실의 위험이 있으며, 모든 책임은 투자자 본인에게 있습니다.
* 실제 투자를 위해 정확한 진단을 원하시면 금융기관의 정식 테스트를 이용해 주세요.
""")

# 3. 질문지 (교수님께서 확정하신 10개 문항 그대로)
questions = [
    "1. 나는 원금을 잃을 위험이 있어도, 은행 이자보다 더 많은 돈을 벌고 싶다.",
    "2. 나는 주식과 ETF가 무엇인지, 그 차이점을 잘 알고 있다.",
    "3. 내 투자금이 10% 줄어들어도 당황하지 않고 침착하게 기다릴 수 있다.",
    "4. 나는 경제 뉴스나 돈에 관한 소식을 자주 찾아본다.",
    "5. 나는 당장 돈을 버는 것보다 1년 이상 길게 보고 투자하는 것이 더 중요하다.",
    "6. 나는 위험이 크더라도 한 번에 큰돈을 벌 수 있는 투자에 매력을 느낀다.",
    "7. 나는 투자의 결과가 좋지 않아도 그것은 나의 선택과 책임임을 알고 있다.",
    "8. 나는 AI나 새로운 기술을 이용해 돈을 관리하는 것에 관심이 많다.",
    "9. 나는 돈을 잃는 위험보다, 돈을 벌 기회를 놓치는 것이 더 걱정된다.",
    "10. 나는 나만의 투자 규칙이 있고, 그것을 끝까지 잘 지킬 수 있다."
]

responses = []

# 4. 질문 생성 (질문 아래에 1~5 버튼 배치 / 모바일 최적화)
st.write("### 📝 질문지")
st.info("1: 전혀 아니다 / 2: 아니다 / 3: 보통이다 / 4: 그렇다 / 5: 매우 그렇다")

for i, q in enumerate(questions):
    # 라디오 버튼을 가로로 배치하여 숫자가 다 보이게 설정
    score = st.radio(
        label=f"**{q}**", # 질문을 위에 표시
        options=[1, 2, 3, 4, 5],
        index=2, # 기본값을 3점으로 설정
        horizontal=True, # 가로 배치로 모바일 터치 최적화
        key=f"q{i}"
    )
    responses.append(score)
    st.write("") # 간격 띄우기

# 5. 결과 확인 버튼 및 로직
if st.button("결과 확인하기"):
    total = sum(responses)
    st.write("---")
    st.subheader(f"🎯 당신의 총점: {total}점")
    
    if 10 <= total <= 23:
        st.success("### 🛡️ 유형 1: 안전 지킴이 (Safety Keeper)")
        st.write("**특징:** 돈을 잃지 않는 것이 가장 중요합니다. 안전한 예금이나 적금을 좋아합니다.")
        
    elif 24 <= total <= 37:
        st.info("### ⚖️ 유형 2: 똑똑한 중간파 (Smart Middle)")
        st.write("**특징:** 안전과 수익의 균형을 잘 잡습니다. 적당한 위험은 견딜 수 있는 영리한 투자자입니다.")
        
    else:
        st.error("### 🚀 유형 3: 용감한 모험가 (Brave Adventurer)")
        st.write("**특징:** 큰 수익을 위해 위험을 두려워하지 않습니다. 미래의 성장을 위해 과감하게 도전합니다.")

st.write("---")
st.write("💡 Professor CJ's Lab")
